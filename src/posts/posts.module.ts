import { CloudinaryModule } from 'src/cloudinary/cloudinary.module';

@Module({
  providers: [PostsService],
  controllers: [PostsController],
  imports: [
    MongooseModule.forFeature([{ name: Post.name, schema: PostSchema }]),
    CloudinaryModule,
  ],
})
export class PostsModule {}
